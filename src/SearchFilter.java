
public class SearchFilter {
	private String Name;
	private String Price;
	
	public void setName(String name)
	{
		this.Name = name;
	}
	public void setPrice(String price)
	{
		this.Price = price;
	}
	
	public String getName()
	{
		return this.Name;
	}
	public String getPrice()
	{
		return this.Price;
	}
}
